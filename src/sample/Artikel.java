package sample;

public class Artikel {
    private String artikelCat;
    private String titel;
    private String stkod;
    private String författare;
    private String reg;
    private String isbn;
    private String hyplnr;
    private String ord;
    private String genrer;
    private String produktionsLand;
    private String ageLimit;
    private String actor;

    public String getArtikelCat() {
        return artikelCat;
    }

    public void setArtikelCat(String artikelCat) {
        this.artikelCat = artikelCat;
    }

    public String getTitel() {
        return titel;
    }

    public void setTitel(String titel) {
        this.titel = titel;
    }

    public String getStkod() {
        return stkod;
    }

    public void setStkod(String stkod) {
        this.stkod = stkod;
    }

    public String getFörfattare() {
        return författare;
    }

    public void setFörfattare(String författare) {
        this.författare = författare;
    }

    public String getReg() {
        return reg;
    }

    public void setReg(String reg) {
        this.reg = reg;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getHyplnr() {
        return hyplnr;
    }

    public void setHyplnr(String hyplnr) {
        this.hyplnr = hyplnr;
    }

    public String getOrd() {
        return ord;
    }

    public void setOrd(String ord) {
        this.ord = ord;
    }

    public String getGenrer() {
        return genrer;
    }

    public void setGenrer(String genrer) {
        this.genrer = genrer;
    }

    public String getProduktionsLand() {
        return produktionsLand;
    }

    public void setProduktionsLand(String produktionsLand) {
        this.produktionsLand = produktionsLand;
    }

    public String getAgeLimit() {
        return ageLimit;
    }

    public void setAgeLimit(String ageLimit) {
        this.ageLimit = ageLimit;
    }

    public String getActor() {
        return actor;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }
}
